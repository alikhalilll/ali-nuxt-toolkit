import { addTemplate } from '@nuxt/kit';

/**
 * Emit a `.d.ts` augmenting page-meta with a typed union of every middleware
 * name referenced by any rule. Lets consumers write `middlewares: ['auth']`
 * in `definePageMeta` and get autocomplete + a compile error on typos.
 */
export function buildTypesTemplate(params: {
  allMiddlewares: string[];
  pageMetaField: string | false;
}) {
  const { allMiddlewares, pageMetaField } = params;

  const unionType =
    allMiddlewares.length > 0 ? allMiddlewares.map((n) => JSON.stringify(n)).join(' | ') : 'never';

  const fieldAugmentation =
    pageMetaField === false
      ? ''
      : `
  interface PageMeta {
    /** Extra middlewares to append after layout-matched ones. */
    ${pageMetaField}?: Array<AutoMiddlewareName>;
    /** When true, skip the auto-middleware dispatcher entirely for this page. */
    skipAutoMiddleware?: boolean;
  }
`;

  return addTemplate({
    filename: 'auto-middleware/types.d.ts',
    getContents: () => `// Auto-generated by @alikhalilll/nuxt-auto-middleware
export type AutoMiddlewareName = ${unionType};

declare module '#app' {
${fieldAugmentation}}

declare module 'nuxt/schema' {
${fieldAugmentation}}

export {};
`,
  });
}
